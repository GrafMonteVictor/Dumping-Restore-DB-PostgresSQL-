#!/bin/bash

source settings.sh
PATHUTILITY=$PATHUTILITY/pg_dump
HOST=$HOST
USER=$USER
PGPASSWORD=$PASSWORD
PATHDUMP=$PATHDUMP
SCHEMAS=$SCHEMAS
STREAMS=$STREAMS
export PGPASSWORD

echo $HOST
echo $USER
echo $PATHUTILITY
echo $SCHEMAS
echo $STREAMS

$PATHUTILITY Postgres -U $USER -h $HOST -p 5432 -n $SCHEMAS -j $STREAMS -Fd -f $PATHDUMP

unset PGPASSWORD
